// Usando o E
console.log(true && true);
console.log(true && false);
console.log(false && false);

// Usando o OU
// dica
//Lembre-se que ao utilizar o operador OU, caso o valor da primeira expressão seja true, o que vier a seguir não será avaliado.
console.log(true || false);
console.log(false || true);
console.log(false || false);

//Não lógico
// O operador lógico NÃO (!) vai mudar o valor posterior a ele, ou seja, true será false e false será true.

console.log(!false);
console.log(!true);