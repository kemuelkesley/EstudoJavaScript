// Criando um calculo básico.

function soma(a, b){
    return a + b;
}
console.log('Valor da soma:', soma(3,4));

function multiplicar(a, b){
    return a * b;
}
console.log('Valor da Multiplicação:', multiplicar(2,2));

function subtrair(a, b){
    return a / b;
}
console.log('Valor da Subtração:', subtrair(4,2));